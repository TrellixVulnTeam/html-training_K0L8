function ifelse (){
    if(document.getElementById("ifelse").style.color = "navy") {
    document.getElementById("ifelse").style.backgroundColor = "brown";
    document.getElementById("ifelse").style.padding = "30px";
    document.getElementById("ifelse").style. border = "2px solid black";
    document.getElementById("ifelse").style. boxShadow = "0px 2px solid black";
}
else {
    
    document.getElementById("ifelse").style.backgroundColor = "white";
    document.getElementById("ifelse").style.padding = "30px";
    document.getElementById("ifelse").style. border = "2px solid black";
    document.getElementById("ifelse").style. boxShadow = "0px 2px solid black";
}

}
